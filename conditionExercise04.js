var perintah = prompt("Masukan perintah anda :");

switch(perintah){

	case "UP" : {console.log("Object berjalan keatas");
	break;
}

	case "RIGHT" : {console.log("Object berjalan ke kanan");
	break;
}

	case "LEFT" : {console.log("Object berjalan ke kiri");
	break;	
}

	case "DOWN" : {console.log("Object berjalan ke bawah");
	break;	
}

	default : {console.log("anda tidak memasukan perintah apa apa");

}
}
